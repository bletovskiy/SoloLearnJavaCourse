public class MultidimensionalArrays {
    public static void main(String[] args) {
        int[][] sample = {
            {1,2,3},
            {4,5,6},
            {7,8,9}};
        int x = sample[1][0];
        System.out.println(x);

        int array[][] = {
        {3, 5, 8}, 
        {7, 54, 1, 12, 4}}; 
        System.out.println(array[0][2]);

    }
}